# Utwórz zmienną nazwisko i przypisz do niej swoje nazwisko.
# Wypisz je w jednym zdaniu:
# Moje imię to [imie], a nazwisko to [nazwisko].

name = 'Olek'
surname = 'Kutycki'
print('Moje imię to,', name, 'a nazwisko to', surname)

# Also it is possible in this way:

name, surname = 'Jan', 'Kowalski'
print('Moje imię to,', name, 'a nazwisko to', surname)