# Zadanie 9. Prosty kalkulator
# Pobierz od użytkownika dwie liczby.
# Oblicz ich sumę, różnicę, iloczyn i iloraz.

first_number = int(input('Proszę wprowadzić pierwszą liczbę: '))
second_number = int(input('Proszę wprowadzić drugą liczbę: '))

print('Suma: ', first_number + second_number)
print('Różnica : ', first_number - second_number)
print('Illoczyn : ', first_number * second_number)
print('Iloraz : ', first_number / second_number)


